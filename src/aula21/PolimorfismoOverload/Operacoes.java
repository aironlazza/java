package aula21.PolimorfismoOverload;

public class Operacoes {
    public static int soma(int n1, int n2){
        return n1+n2;
    }
    public static int soma(int n1, int n2, int n3){
        return n1+n2+n3;
    }
    public static String soma(String s1, String s2){
        return s1+s2;
    }
}
